/**
 * The Media enumeration records the names of the available Book media formats.
 * Values are:
 * <PRE>
 * Paperback
 * Hardcover
 * Cassette
 * Audio
 * Electronic
 * </PRE>
 *
 * @author ben k steele bks@cs.rit.edu, Joseph Cumb jwc999@rit.edu
 */
public enum Media {

    Paperback,
    Hardcover,
    Cassette,
    Audio,
    Electronic;
}

